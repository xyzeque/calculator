const add = (a, b) => {
  const sum = a + b;
  return sum;
}

const subtract = (a, b) => {
  const diff = a - b;
  return diff;
}

const multiply = (a, b) => {
  const product = a * b;
  return product;
}

const divide = (a, b) => {
  const quotient = a / b;
  return quotient;
}

console.log(add(6,9));
console.log(subtract(6,9));
console.log(multiply(6,9));
console.log(divide(6,9));
